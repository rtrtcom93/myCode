#include <iostream>
#include <string>
using namespace std;

int main() {
    char character = 'C'; // 문자형 변수 선언 및 초기화
    int integer = 42; // 정수형 변수 선언 및 초기화
    double precision = 3.14159; // 실수형 변수 선언 및 초기화
    bool is_true = true; // 불리언 변수 선언 및 초기화
    string word = "Hello"; // 문자열 변수 선언 및 초기화

    cout << "char : " << character << endl; // 문자형 출력
    cout << "int : " << integer << endl; // 정수형 출력
    cout << "double : " << precision << endl; // 실수형 출력
    cout << "bool : " << is_true << endl; // 불리언 출력
    cout << "string : " << word << endl; // 문자열 출력

    return 0;
}