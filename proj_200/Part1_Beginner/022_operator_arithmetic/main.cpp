#include <iostream>
using namespace std;

int main () {
    /* == +,- arithmetic operator ==
     +,- 대수 연산자는 binary operator (이항 연산자)로 사칙연산 중 덧셈과 뺄셈을 수행
    */
    int x = 1;
    int y = -1;
    int z = -2;

    cout << "1 + (-1) = " << x + y << endl;
    cout << "-1 + -2 = " << y + z << endl;

    return 0;
}